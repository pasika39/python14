#สร้าางฟังก์ชั่น
def square(width,height):
    ans = width*height
    print(ans)
square(4,4)
#คำสั่งหาพื้นที่สี่เหลี่ยม
def triangle(base,height):
    ans = 1/2*base*height
    print(ans)
triangle(5,12)
#คำสั้งหาพื้นที่ววงกลม
def circle(radius):
    ans = 3.14*(radius*radius)
    print(ans)
circle(7)
